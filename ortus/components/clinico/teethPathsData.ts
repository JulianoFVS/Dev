/**
 * Maps an FDI tooth number (11-48) to the tooth type index (0-7).
 * The unit digit determines the type:
 *   1 → Central Incisor (0)
 *   2 → Lateral Incisor (1)
 *   3 → Canine (2)
 *   4 → First Premolar (3)
 *   5 → Second Premolar (4)
 *   6 → First Molar (5)
 *   7 → Second Molar (6)
 *   8 → Third Molar (7)
 */
export function toothTypeIndex(fdiNumber: number): number {
  return (fdiNumber % 10) - 1;
}

/**
 * Returns true if the FDI number belongs to the upper arch (maxilla).
 */
export function isUpperArch(fdiNumber: number): boolean {
  const decade = Math.floor(fdiNumber / 10);
  return decade === 1 || decade === 2;
}
